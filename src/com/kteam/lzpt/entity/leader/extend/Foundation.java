package com.kteam.lzpt.entity.leader.extend;

public class Foundation {

private String id;
	
	private String leaderInfoId;	
	
	private String callName;
	
	private String userName;
	
	private String foundationName;
	
	private String foundationNumber;
	
	private Double capitalization;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getLeaderInfoId() {
		return leaderInfoId;
	}

	public void setLeaderInfoId(String leaderInfoId) {
		this.leaderInfoId = leaderInfoId;
	}

	public String getCallName() {
		return callName;
	}

	public void setCallName(String callName) {
		this.callName = callName;
	}

	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}

	public String getFoundationName() {
		return foundationName;
	}

	public void setFoundationName(String foundationName) {
		this.foundationName = foundationName;
	}


	public String getFoundationNumber() {
		return foundationNumber;
	}

	public void setFoundationNumber(String foundationNumber) {
		this.foundationNumber = foundationNumber;
	}

	public Double getCapitalization() {
		return capitalization;
	}

	public void setCapitalization(Double capitalization) {
		this.capitalization = capitalization;
	}
}
