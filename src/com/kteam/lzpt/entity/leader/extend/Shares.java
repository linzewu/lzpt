package com.kteam.lzpt.entity.leader.extend;

public class Shares {
	
	private String id;
	
	private String leaderInfoId;	
	
	private String callName;
	
	private String userName;
	
	private String shareName;
	
	private String shareNumber;
	
	private Double capitalization;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getLeaderInfoId() {
		return leaderInfoId;
	}

	public void setLeaderInfoId(String leaderInfoId) {
		this.leaderInfoId = leaderInfoId;
	}

	public String getCallName() {
		return callName;
	}

	public void setCallName(String callName) {
		this.callName = callName;
	}

	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}

	public String getShareName() {
		return shareName;
	}

	public void setShareName(String shareName) {
		this.shareName = shareName;
	}


	public String getShareNumber() {
		return shareNumber;
	}

	public void setShareNumber(String shareNumber) {
		this.shareNumber = shareNumber;
	}

	public Double getCapitalization() {
		return capitalization;
	}

	public void setCapitalization(Double capitalization) {
		this.capitalization = capitalization;
	}

	
}
