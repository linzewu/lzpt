package com.kteam.lzpt.entity.leader.extend;

public class Futures {
	private String id;
	
private String leaderInfoId;	
	
	private String callName;
	
	private String userName;
	
	private String futuresType;
	
	private Double futuresNumber;
	
	private Double futureUse;
	
	private Double futureAvailable;

	public String getLeaderInfoId() {
		return leaderInfoId;
	}

	public void setLeaderInfoId(String leaderInfoId) {
		this.leaderInfoId = leaderInfoId;
	}

	public String getCallName() {
		return callName;
	}

	public void setCallName(String callName) {
		this.callName = callName;
	}

	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getFuturesType() {
		return futuresType;
	}

	public void setFuturesType(String futuresType) {
		this.futuresType = futuresType;
	}

	public Double getFuturesNumber() {
		return futuresNumber;
	}

	public void setFuturesNumber(Double futuresNumber) {
		this.futuresNumber = futuresNumber;
	}

	public Double getFutureUse() {
		return futureUse;
	}

	public void setFutureUse(Double futureUse) {
		this.futureUse = futureUse;
	}

	public Double getFutureAvailable() {
		return futureAvailable;
	}

	public void setFutureAvailable(Double futureAvailable) {
		this.futureAvailable = futureAvailable;
	}

	
}
